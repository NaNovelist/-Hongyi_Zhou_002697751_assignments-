    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblAccounts.getModel();//table model is used to manipulate model content
        model.setRowCount(0);
        
        for (BankAccount ba : account.getAccount()){//get here(after click) means return an arraylist of homeaddress
            
            Object[] row = new Object[6];
            row[0] = ba;//lab 2 have the whole object, not only the date??
            row[1] = ba.getName();
            row[2] = ba.getAccountType();
            row[3] = ba.getAccountNumber();
            row[4] = ba.getOpeningDate();
            row[5] = ba.getCreditAmount();
            
            model.addRow(row);
        }
        
    }
