    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblAccounts.getSelectedRow();
        
        if (selectedRowIndex<0) {           
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblAccounts.getModel();
        BankAccount selectedAccounts = (BankAccount) model.getValueAt(selectedRowIndex, 0);//getValueAt() can pick any of the cells in table
        
        account.deleteAccounts(selectedAccounts);
        
        JOptionPane.showMessageDialog(this, "Bank Account deleted.");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

